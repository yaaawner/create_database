USE [Library_2021]
GO

IF OBJECT_ID('IsAvailable') IS NOT NULL
	DROP PROCEDURE [IsAvailable]
GO

CREATE PROC dbo.IsAvailable
    @book_id int
AS
IF EXISTS 
  (SELECT * FROM [Books] WHERE @book_id = [Book ID] 
  AND [Available] > 0)
     RETURN (1) 
ELSE RETURN (0)
GO